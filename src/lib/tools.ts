// Helper functions
export const toolList = Object.values(tools);
export const featuredTools = toolList.filter(t => t.featured);

export function getToolBySlug(slug: string): Tool | undefined {
  return tools[slug];
}

export function getToolsByCategory(category: CategorySlug): Tool[] {
  return toolList.filter(t => t.category === category);
}

export function getSubConversionBySlug(slug: string): SubConversion | undefined {
  for (const tool of toolList) {
    const conversion = tool.popularConversions.find(c => c.slug === slug);
    if (conversion) return conversion;
  }
  return undefined;
}

export function getToolForSubConversion(slug: string): Tool | undefined {
  for (const tool of toolList) {
    if (tool.popularConversions.some(c => c.slug === slug)) {
      return tool;
    }
  }
  return undefined;
}

export function getAllSubConversions(): Array<SubConversion & { toolSlug: string }> {
  const all: Array<SubConversion & { toolSlug: string }> = [];
  for (const tool of toolList) {
    for (const conv of tool.popularConversions) {
      all.push({ ...conv, toolSlug: tool.slug });
    }
  }
  return all.sort((a, b) => b.priority - a.priority);
}
